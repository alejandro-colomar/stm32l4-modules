/******************************************************************************
 ******* global functions *****************************************************
 ******************************************************************************/
	/**
	 * @brief	Test TIM periodic interrupts
	 * @param	period_us:	period of the interrupts
	 * @return	Error
	 */
int	tim_test	(void)
{
	float	period_us;

	period_us	= 65000u;

	if (delay_us_init()) {
		return	ERROR_NOK;
	}
	led_init();
	if (tim_tim3_init(period_us)) {
		return	ERROR_NOK;
	}

	if (execution_loop(period_us)) {
		return	ERROR_NOK;
	}

	return	ERROR_OK;
}


/******************************************************************************
 ******* static functions (definitions) ***************************************
 ******************************************************************************/
static	void	flash		(float period_us)
{

	led_set();
	delay_us(period_us * 0.3);
	led_reset();
	delay_us(period_us * 0.3);
}

static	int	execution_loop	(float period_us)
{

	while (true) {
		__WFI();

		if (tim_tim3_interrupt) {
			flash(period_us);

			tim_tim3_interrupt	= false;
		}
	}

	return	ERROR_OK;
}


